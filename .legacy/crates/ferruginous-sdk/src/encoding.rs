/// Gets the corresponding character from a standard encoding name.
pub fn get_standard_char(encoding_name: &str, byte: u8) -> Option<char> {
    match encoding_name {
        "WinAnsiEncoding" => Some(WIN_ANSI_ENCODING[byte as usize]),
        "MacRomanEncoding" => Some(MAC_ROMAN_ENCODING[byte as usize]),
        _ => None,
    }
}
